console.log("#################### OPERAÇÕES LÓGICAS EM JAVASCRIPT #################")

console.log('Tabela variedade do E (&&) lógico');
console.log(`true && true: ${true && true}`);
console.log(`true && false: ${true && false}`);
console.log(`false && true: ${false && true}`);
console.log(`false && false: ${false && false}`);

console.log('\n');

console.log('Tabela Verdade do OU (||) lógico');
console.log(`true || true: ${true || true}`);
console.log(`true || false: ${true || false}`);
console.log(`false || true: ${false || true}`);
console.log(`false || false: ${false || false}`);

console.log("\n");

console.log('Tabela Verdade da NEGAÇÃO (!) lógica');
console.log(`!true ${!true}`);
console.log(`!false ${!false}`);